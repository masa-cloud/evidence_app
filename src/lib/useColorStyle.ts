export const useFontColor = (color: string): { color: string } => {
  return {
    color,
  };
};

export const useBgColor = (color: string): { backgroundColor: string } => {
  return {
    backgroundColor: color,
  };
};

export const useBorderColor = (color: string): { borderColor: string } => {
  return {
    borderColor: color,
  };
};
